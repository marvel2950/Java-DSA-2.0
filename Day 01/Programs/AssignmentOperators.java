public class AssignmentOperators {

    public static void main(String[] args) {
        int a = 6;

        a = 7 + 1;

        System.out.println(a);

        int b = 7;

        a = a + b;
        System.out.println(a);

        a = a + 1;
        System.out.println(a);

        a += 1;
        System.out.println(a);

        a -= 5;
        System.out.println(a);

    }
}